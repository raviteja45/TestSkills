/**
 * Definition for a binary tree node.
 * public class TreeNode {
 *     int val;
 *     TreeNode left;
 *     TreeNode right;
 *     TreeNode() {}
 *     TreeNode(int val) { this.val = val; }
 *     TreeNode(int val, TreeNode left, TreeNode right) {
 *         this.val = val;
 *         this.left = left;
 *         this.right = right;
 *     }
 * }
 */
class Solution {
    public TreeNode invertTree(TreeNode root) {
        if(root==null){
            return null;
        }
        TreeNode m = root;
        Queue<TreeNode> q = new LinkedList<>();
        q.add(m);
        while(q.size()>0){
            int n = q.size();
            for(int i=0;i<n;i++){
                TreeNode t = q.poll();
                TreeNode temp = t.left;
                t.left = t.right;
                t.right = temp;
                if(t.left!=null){
                    q.add(t.left);
                }
                if(t.right!=null){
                    q.add(t.right);
                }
            }
        }
        return m;
    }
}


